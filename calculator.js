function soma(num1, num2) {
  return num1 + num2;
}

function subtracao(num1, num2) {
  return num1 - num2;
}

function multiplicacao(num1, num2) {
  return num1 * num2;
}

function divisao(num1, num2) {
  return num1 / num2;
}

exports.multiplicacao = multiplicacao;
exports.divisao = divisao;
exports.soma = soma;
exports.subtracao = subtracao;
